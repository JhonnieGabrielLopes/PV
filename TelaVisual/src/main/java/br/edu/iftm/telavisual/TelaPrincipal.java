    //variavel para determinar o operador de calculo
    private int operador;
    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        float valor1, valor2;
        valor1 = Float.parseFloat(entrada1.getText());
        valor2 = Float.parseFloat(entrada2.getText());
        switch (operador) {
            case 1:
                JOptionPane.showMessageDialog(jPanelPrincipal, "RESULTADO: "+(valor1+valor2));
            break;
            case 2:
                JOptionPane.showMessageDialog(jPanelPrincipal, "RESULTADO: "+(valor1*valor2));
            break;
            case 3:
                JOptionPane.showMessageDialog(jPanelPrincipal, "RESULTADO: "+(valor1/valor2));
            break;
            case 4:
                JOptionPane.showMessageDialog(jPanelPrincipal, "RESULTADO: "+(valor1-valor2));
            break;
            default:
                JOptionPane.showMessageDialog(jPanelPrincipal, "SELECIONE UM OPERADOR", "ERRO", JOptionPane.ERROR_MESSAGE);
            break;
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

