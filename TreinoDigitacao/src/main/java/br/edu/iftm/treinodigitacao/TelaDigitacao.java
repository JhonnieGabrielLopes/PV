    private void panelPrincipalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_panelPrincipalKeyReleased
        for(Component c : panelPrincipal.getComponents()){
            if(c instanceof JButton){
                JButton botao = (JButton) c;
                String temp = botao.getText();
                if("CAPS".equalsIgnoreCase(temp)){
                    boolean capslock = Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_CAPS_LOCK);
                    if(capslock){
                        continue;
                    }
                }
                botao.setBackground(Color.DARK_GRAY);
            }
        }
    }//GEN-LAST:event_panelPrincipalKeyReleased

