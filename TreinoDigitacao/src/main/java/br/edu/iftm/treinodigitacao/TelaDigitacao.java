    private void panelPrincipalKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_panelPrincipalKeyPressed
        char tecla = evt.getKeyChar();
        int codigo = evt.getKeyCode();
        switch (codigo){
            case KeyEvent.VK_TAB:
                destacarTeclaDif("TAB");
                break;
            case KeyEvent.VK_ENTER:
                destacarTeclaDif("ENTER");
                break;
            case KeyEvent.VK_BACK_SPACE:
                destacarTeclaDif("BACKSPACE");
                String temp = entradaTexto.getText();
                if(!temp.isEmpty()){
                    temp = temp.substring(0, temp.length() - 1);
                    entradaTexto.setText(temp);
                }
                break;
            case KeyEvent.VK_SPACE:
                destacarTeclaDif("SPACE");
                break;
            case KeyEvent.VK_CAPS_LOCK:
                destacarTeclaDif("CAPS");
                break;
            case KeyEvent.VK_SHIFT:
                destacarTeclaDif("SHIFT");
                break;
            case KeyEvent.VK_DEAD_TILDE:
                destacarTeclaDif("~");
                break;
            case KeyEvent.VK_UP:
                destacarTeclaDif("^");
                break;
            case KeyEvent.VK_DOWN:
                destacarTeclaDif("SETA_BAIXO");
                break;
            case KeyEvent.VK_RIGHT:
                destacarTeclaDif(">");
                break;
            case KeyEvent.VK_LEFT:
                destacarTeclaDif("<");
                break;
            default:
                destacarTecla(tecla);
        }
        
    }//GEN-LAST:event_panelPrincipalKeyPressed

